#!/usr/bin/env node
// AlexがローカルでX投稿を生成（毎朝7:00 cron実行）
const Anthropic = require('@anthropic-ai/sdk');
const fs = require('fs');
const path = require('path');

const anthropic = new Anthropic({
  apiKey: process.env.ANTHROPIC_API_KEY
});

async function generateDailyPosts() {
  console.log('📝 Alex: Generating daily X posts...\n');

  const today = new Date().toISOString().split('T')[0];
  const outputDir = path.join(__dirname, '../scheduled-posts', today);
  
  // ディレクトリ作成
  fs.mkdirSync(outputDir, { recursive: true });

  // トラクションデータ読み込み
  const tractionPath = path.join(__dirname, '../data/current-traction.json');
  let traction = { users: 127, etfs: 8, environment: 'devnet' };
  
  if (fs.existsSync(tractionPath)) {
    traction = JSON.parse(fs.readFileSync(tractionPath, 'utf8'));
  }

  // 過去投稿パターン読み込み
  const patternsPath = path.join(__dirname, '../data/top-patterns.json');
  let patterns = null;
  
  if (fs.existsSync(patternsPath)) {
    patterns = JSON.parse(fs.readFileSync(patternsPath, 'utf8'));
  }

  console.log(`Current traction: ${traction.users} users, ${traction.etfs} ETFs`);

  // Claude APIで投稿生成
  const message = await anthropic.messages.create({
    model: 'claude-sonnet-4',
    max_tokens: 4000,
    messages: [{
      role: 'user',
      content: `You are Alex, Muse's executive assistant generating X posts for @muse_jp_sol.

## Current Traction (DevNet)
- Users: ${traction.users}
- ETFs: ${traction.etfs}

## Task
Generate 4 X posts for today based on Muse's proven style:

1. **Post 1 (JST 1:30)** - Personal story + traction
2. **Post 2 (JST 3:00)** - Technical progress
3. **Post 3 (JST 5:00)** - Japanese community update
4. **Post 4 (JST 7:00)** - Reaction/surprise

## Muse's Style (from 632 tweets)
- Average 99 chars (but 100-200 performs best)
- Personal stories get 85% of high engagement
- No hashtags (0% in top 20)
- Japanese posts get +28% engagement
- Exclamation marks in 45% of top posts

## Output Format (JSON)
{
  "01-30": "tweet text",
  "03-00": "tweet text",
  "05-00": "tweet text",
  "07-00": "tweet text"
}

Make them authentic, using real traction numbers (${traction.users} users, ${traction.etfs} ETFs), matching Muse's voice.`
    }]
  });

  const response = message.content[0].text;
  
  // JSONを抽出
  const jsonMatch = response.match(/\{[\s\S]*\}/);
  if (!jsonMatch) {
    throw new Error('Failed to parse JSON from Claude response');
  }

  const posts = JSON.parse(jsonMatch[0]);

  // 各投稿をファイルに保存
  for (const [time, text] of Object.entries(posts)) {
    const filePath = path.join(outputDir, `${time}.txt`);
    fs.writeFileSync(filePath, text, 'utf8');
    console.log(`✅ ${time}: ${text.substring(0, 50)}...`);
  }

  console.log(`\n📁 Saved to: ${outputDir}`);
  
  // GitHubにコミット
  console.log('\n📤 Committing to GitHub...');
  const { execSync } = require('child_process');
  
  try {
    execSync('git add .company/marketing/scheduled-posts/', { cwd: path.join(__dirname, '../../..') });
    execSync(`git commit -m "📝 Daily posts generated by Alex: ${today}"`, { cwd: path.join(__dirname, '../../..') });
    execSync('git push', { cwd: path.join(__dirname, '../../..') });
    console.log('✅ Pushed to GitHub');
  } catch (e) {
    console.log('⚠️ Git commit failed (may be no changes)');
  }

  return posts;
}

generateDailyPosts().then(posts => {
  console.log('\n✅ Alex: Daily posts ready for review');
}).catch(error => {
  console.error('❌ Error:', error.message);
  process.exit(1);
});
